package array;

public class MagneticForceBetweenTwoBalls {
    public int maxDistance(int[] position, int m) {
        int n=position.length;
        int start=0;
        int end=n;
        int mid=start+(end-start)/2;
        if(mid-start==end-mid){
            return end-mid;
        }
   
        
    }
    
}
